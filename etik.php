          <h2>ETİK İLKELER ve YAYIN POLİTİKASI</h2>
    <p>Dergimiz, etik görev ve sorumluluklarını yerine getirirken "Yükseköğretim Kurumları Bilimsel Araştırma ve Yayın Etiği Yönergesi" ve "Committee on Publication Ethics (COPE)" tarafından belirlenen ilkeleri titizlikle uygulamaktadır.</p>
    <p>Dergimize gönderilen çalışma, baş editör tarafından öncelikle şekil ve içerik açısından ön incelemeye tabi tutulur ve derginin kurallarına uygun olduğu takdirde, ilgili alandaki en az iki hakeme gönderilir. Hakem değerlendirmesi çift-kör hakemlik sürecine göre yürütülür, değerlendirmede yazar ve hakem bilgilerinin gizliliği esastır. Hakemlerden gelen raporlar doğrultusunda, makalenin yayınlanmasına (kabul), düzeltme talep edilmesine (düzeltme) veya yayınlanmamasına (ret) karar verilir ve bu karar yazarlara en geç 2 ay içinde bildirilir. Düzeltme istenen makaleler, yazar veya yazarlar tarafından belirlenen süre içinde düzeltilmezse yayınlanmaz. Hakem değerlendirme sürecinde olumlu sonuçlanan eserler yayımlanmak üzere sıraya alınır. Makalelerdeki görüşler ve bilimsel sorumluluklar yazar veya yazarlara aittir ve dergi ile herhangi bir ilişkisi bulunmamaktadır. Makalenin yayımlanmasına karar verildiğinde, yazarlar yayın haklarını dergiye devretmiş olurlar. Dergide yayımlanan yazılar, dergi yönetiminin yazılı izni olmadan hiçbir şekilde çoğaltılamaz veya başka bir yerde basılı veya elektronik olarak yeniden yayımlanamaz. Makale gönderen yazarlar, bu ilkeleri kabul etmiş sayılırlar.</p>

    <h3>Yazarların Etik Sorumluluklar</h3>
    <ul>
        <li>Yazarlar dergiye gönderdikleri makaleleri başka bir yerde yayınlamamış ya da yayınlanmak üzere göndermemiş olmalıdır.</li>
        <li>Yazarlar makalelerinde kullandıkları tüm alıntılara kaynak göstermiş olmalıdır.</li>
        <li>Yazarlar makalenin kendi çalışmaları olduğunu, hiçbir şekilde intihal yapmadıklarını, intihalden doğan tüm sorumlulukların kendilerine ait olduğunu, bu konuda derginin hiçbir sorumluluğunun olmadığını beyan etmiş olmaktadır.</li>
        <li>Yazarlar makaleye bilimsel katkı verdiklerini garanti etmeli ve tüm yazarların makale üzerinde eşit sorumluluğa sahip olduğu bilinmelidir.</li>
        <li>Sorumlu yazar makalede adı geçen tüm ortak yazarların yayına ve ortak yazar olarak adlandırılmaya razı olduğunu garanti etmelidir. Çalışmaya önemli katkılar sağlayan tüm kişiler ortak yazar olarak adlandırılmalıdır. Bunun dışındaki kişilere teşekkür kısmında yer verilmelidir.</li>
        <li>Yazarlar gönderdikleri çalışmaları destekleyen kuruluşları, finansal kaynakları veya çıkar çatışmasını beyan etmekle yükümlüdür.</li>
        <li>Yazarlar gerekli görülmesi halinde makalede yer alan veri setlerine ulaşım imkanı sağlamalıdır.</li>
        <li>Yazarlar dergide yayımlanmak üzere gönderilen makaleleri araştırma ve yayın etiği kurallarına uygun olarak hazırlamış olmalıdır. Yapılan araştırmalar için ve etik kurul kararı gerektiren klinik ve deneysel insan ve hayvanlar üzerindeki çalışmalar için ayrı ayrı etik kurul onayı alınmış olmalı, bu onay makalede belirtilmeli ve belgelendirilmelidir.</li>
    </ul>

    <h3>Hakemlerin Etik Sorumlulukları</h3>
    <ul>
        <li>Araştırmayı incelemek için yeterli nitelikte olmayan veya makaleyi hızlı bir şekilde değerlendirmesinin imkânsız olacağını bilen seçilmiş herhangi bir hakem, editöre bu durumu bildirmeli ve inceleme sürecinden mazurunu istemelidir.</li>
        <li>Hakemler kendilerine ulaşan makaleleri gizli tutmak ve hakemlik sürecinden elde ettikleri bilgileri kişisel menfaatleri için kullanmamakla yükümlüdürler.</li>
        <li>Hakemler raporlarını veya makale hakkındaki bilgileri başkalarıyla paylaşmamalıdırlar ve editörün izni olmadan yazarlarla doğrudan iletişim kurmamalıdırlar.</li>
        <li>Hakemler makaledeki potansiyel etik meseleler konusunda özenli olmalı ve bunları editörün dikkatine sunmalıdır.</li>
        <li>Hakemlik nesnel bir şekilde yapılmalıdır. Yazarlara dair kişisel eleştirilerde bulunulmamalı, değerlendirmeler yapıcı, dürüst ve kibar olmalıdır.</li>
        <li>Hakemler yazarlarından herhangi biriyle rekabetçi, işbirlikçi veya başka türlü bir ilişki veya bağlantıdan kaynaklanabilecek potansiyel bir çıkar çatışmasına sahip olduğu bir makaleyi değerlendirmeyi kabul etmeden önce editöre danışmalıdır.</li>
    </ul>

    <h3>Editör Kurulunun Etik Sorumlulukları</h3>
    <ul>
        <li>Dergide hangi makalelerin yayınlanacağına karar vermek editör kurulunun sorumluluğundadır. Yazarların makalelerini değerlendiren editör kurulu; ırk, cinsiyet, cinsel yönelim, dini inanç, etnik köken, vatandaşlık ya da politik felsefelerinden bağımsız olarak değerlendirme yapmalıdır.</li>
        <li>Alınacak karar, makalenin doğruluğu, geçerliliği ve önemi ile derginin kapsamının uygunluğuna dayanmalıdır. İftira, telif hakkı ihlali ve intihal ile ilgili mevcut yasal gereklilikler de dikkate alınmalıdır.</li>
        <li>Editör veya herhangi bir editör kurulu üyesi, ilgili yazar, hakemler, potansiyel hakemler, diğer editör danışmanları ve yayıncılardan başka kimseye bir makale hakkında bilgi ifşa etmemelidir.</li>
        <li>Sunulan bir makalede açıklanan yayınlanmamış malzemeler, yazarın açık yazılı izni olmaksızın editörün veya editör kurulunun kendi araştırmalarında kullanılmamalıdır.</li>
    </ul>

