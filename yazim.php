    <h3>Genel Gönderim Kuralları</h3>
    <ol>
        <li>Gönderilen çalışmaların daha önce hiçbir yerde yayınlanmamış olması zorunludur.</li>
        <li>Çalışma, Microsoft Word, Google Docs ya da Libre Office programlarında yazılabilir. Sisteme Microsoft Word (.doc veya .docx uzantılı) formatında yüklenmelidir. Sayfa tasarımları dosya yüklenmeden önce kontrol edilmelidir.</li>
        <li>Yazarlarımız, makalelerini hazırlarken makale şablonunu kullanmalıdır. Şablon formatına ve aşağıdaki talimatlara uygun hazırlanmamış makaleler değerlendirmeye alınmayacaktır.</li>
        <li>Dergimizin değerlendirme sürecinde kör hakemlik politikası uygulandığından yazar bilgileri makalenin hiçbir bölümünde görünmemelidir.</li>
        <li>Makale ekler ve kaynakça dâhil 25 sayfayı geçmeyecek şekilde ve sayfa numarası kullanılmadan hazırlanmalıdır.</li>
        <li>Formüller ve denklemler, denklem editörü ile yazılmalıdır. Tüm denklemlere sıra numarası verilmelidir. Formüller ve denklemler sola yaslı yazılmalıdır. Sıra numarası parantez içinde ve satırın en sağında bulunmalıdır, diğer denklem numaralarıyla aynı hizada olmalıdır.</li>
        <li>Yazı karakteri başlıklar 12 punto, metin 10 punto “Times New Roman” biçiminde olmalıdır. Metin tek satır aralıklı ve iki yana dayalı olarak yazılmalıdır. Başlıklarda otomatik numaralandırma yapılmamalıdır.</li>
        <li>Sayfa yapısı “normal”, üsten 2.5 cm, alttan 2.5 cm, sağ 2.5 cm, sol 2.5 cm, girinti 1 cm olmalıdır.</li>
        <li>Kaynaklar 10 punto olarak yazılmalı ve sonucun hemen ardından makalenin sonunda yer almalıdır.</li>
        <li>Göndermeler dipnotlar şeklinde değil, (APA) metin içinde ilgili yere açılacak parantezlerle yapılmalıdır. Parantez içindeki sıralama yazar(lar)ın soyadı, kaynağın yılı, sayfa numarası ya da numaraları şeklinde olmalıdır.</li>
        <li>Kaynakçada kaynaklar yazar soyadına göre alfabetik olarak sıralanmalıdır.</li>
        <li>İnternet kaynaklarında yazar ismi bulunmuyorsa, bu kaynaklar, kaynakçada yazarı belirli kaynaklar sıralaması sona erdikten sonra «İnternet Kaynakları» başlığında erişim tarihleri esasına göre sıralanmalıdır.</li>
        <li>Tablo, şekil, grafik ve resim için, alıntı yapılmışsa, kaynak mutlaka belirtilmelidir. Gösterilecek kaynak, tablo, şekil, grafik ve resmin hemen altında, 10 punto, 1 satır aralıklı olarak belirtilmelidir.</li>
        <li>Tablo, şekil, grafik ve resimlerin adları, tablo, şekil, grafik ve resimlerin sınırlarını aşmayacak şekilde üstte, Times New Roman, 10 punto, kalın, 1 satır aralıklı, sözcüklerin baş harfleri büyük olarak yazılmalıdır.</li>
        <li>Editör ve yayın kurulu, makalenin özünü değiştirmeksizin makalenin dergi yazım kurallarına uyması açısından gerekli olan değişiklikleri (yazı puntosu, yazı biçimi, sayfa no verme vb.) yapabilir.</li>
        <li>Kaynakçanın, paragraf ayarları/girinti özelliklerinin asılı ve 1 cm olmasına dikkat edilmelidir.</li>
        <li>Kaynakça 10 punto şeklinde çalışmanın sonunda düzenlenmelidir. Kaynakça makale metninden sonraki sayfadan başlatılmalı ve çalışmalar soyadına göre alfabetik olarak sıralanmalıdır. Aynı yazarın birden çok çalışmasına atıfta bulunulduğunda, yayın tarihi en eski olandan başlamalıdır. Aynı yazarın aynı tarihli birden fazla çalışmasına atıfta bulunulduğunda kaynaklar kaynakça ve metin içi atıflarda a,b,c,… harfleri kullanılarak numaralandırılmalıdır. Örnek; 2010a, 2010b, 2010c gibi. Bir yazarın tek ve birden fazla yazarlı çalışmasına atıfta bulunulması durumunda, önce tek yazarlı çalışmalar belirtilmelidir. Kaynakların yazılmasında standart APA 6 formatı kullanılmalıdır.</li>
        <li>Ekler kaynaklardan sonra makalenin sonuna yerleştirilmelidir.</li>
        <li>İstenilen özellikleri içermeyen yazılar değerlendirilmeye alınmayacaktır.</li>
        <li>Aynı yazara ait tek yazarlı birden fazla yazı aynı sayı içerisinde yayınlanamaz.</li>
